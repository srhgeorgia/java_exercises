package exerciciosVariaveis;

public class prog2 {

	public static void main(String[] args) {

		int num = 5;
		
		num = num += 3;
		System.out.println(num);
		//num = 8
		
		num -= 1;
		System.out.println(num);
		//num = 7
		
		num *= 2;
		System.out.println(num);
		//num = 14
		
		num /= 2;
		System.out.println(num);
		//num = 7
		
		num ++;
		System.out.println(num);
		//num = 8
		
		num --;
		System.out.println(num);
		//num = 7
	}

}
